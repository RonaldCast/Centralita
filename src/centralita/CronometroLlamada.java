    private void iniciarCronometro() {
        Thread hilo = new Thread() {
            public void run() {
                for (;;) {
                    if (estado) {
                        try {
                           sleep(1000);
                           seg++;
                        } catch (Exception e) {

                        }
                    }else{
                        break;
                    }
                  lbCont.setText("" + seg);
                }
            }
        };
       hilo.start();
    }


